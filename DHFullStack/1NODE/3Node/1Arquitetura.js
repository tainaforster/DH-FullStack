// >> Arquitetura Client-side

// A expressao Client-side é utilizada no contexto de desenvolvimento de sites.
// Chamamos de CLIENTE o dispositivo que faz solicitaçoes de determinados serviços e recursos
// para um servidor. Esse dispositivo, no caso cliente, pode ser um computadorm telefone, tablet,
// console de video game ou qualquer aparelho capaz de se conectar em uma rede.

// A ideia de um servidor faz referencia a uma equipe que presta serviços aos dispositivos que
// estao conectados, muitas vezes o que ele fornece é uma pagina da web.

// A Arquitetura Client-side tem uma referencia a um modelo de comunicaçao que vincula varios 
// dispositivos com o servidor atraves de uma rede (internet). 
// Dentro dessa estrutura como clientes acessamos os serviços de um servidor atraves de uma conexao
// da internet e utilizando um navegador ou browser (chrome, mozilla, safari...)

// REQUEST - pedido do cliente
// RESPONSE - resposta do servidor


//  O servidor tem um Webserver que é um programa encarregado de processar solicitaçoes feitas pelo
// clientes, gerar e enviar as respostas.




//  A maioria das aplocaçoes tem duas areas: Front-end e Back-end


// ** Front-end - é tudo o que nosso usuario ve, incluindo todos os elementos graficos que compoem
// a interface do site. 
//  HTML para estruturar as paginas
//  CSS para criar os estilos visuais 
//  JavaScript para criar interaçoes/ programaçao dentro do site (cliques, alertas...)


// ** Back-end - é tudo aquilo que acontece dentro do nosso servidor. É o que permite que todos os 
// requests solicitados pelo cliente sejam processados e respondidos.
//  PHP para sites dinamicos
//  MySQL para manipulaçai de banco de dados
//  C Sharp 
//  Java
//  Json e xml para lidar com informaçoes dentro de arquivos
//  JavaScript


// Usar o Javascript do lado mdo servidor é uma proeza chamada NODE.JS. Permite programar na mesma 
// linguagem tanto no front como no back, tornando o processo de desenvolvimento mais fluido. 

// O computador usado para programar pode fazer o mesmo trabalho do servidor e também o do cliente.

//  Quando instalamos um servidor no nosso computador, conseguimos simular todo o caminho que
//  acontece dentro de um sistema. Simular o caminho de siliciraçoes e respostas (Request e Response)
//  Isso vai nos permitir ter um ambiente local para desenvolver, testar e criar as aplicaçoes antes
//  de enviar para um servidor real. 